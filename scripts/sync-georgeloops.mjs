async function githubJson(url) {
  const response = await fetch(url, {
    headers: process.env.GITHUB_TOKEN
      ? { Authorization: `Bearer ${process.env.GITHUB_TOKEN}` }
      : undefined,
  });

  if (!response.ok) {
    throw new Error(`GitHub request failed: ${response.status} ${url}`);
  }

  return response.json();
}

async function githubText(url) {
  const response = await fetch(url, {
    headers: process.env.GITHUB_TOKEN
      ? { Authorization: `Bearer ${process.env.GITHUB_TOKEN}` }
      : undefined,
  });

  if (!response.ok) {
    throw new Error(`GitHub raw request failed: ${response.status} ${url}`);
  }

  return response.text();
}

function parseFrontmatter(markdown) {
  const match = markdown.match(/^---\n([\s\S]*?)\n---\n?/);
  const fields = {};

  if (!match) {
    return fields;
  }

  for (const line of match[1].split("\n")) {
    const field = line.match(/^([A-Za-z0-9_-]+):\s*(.*)$/);
    if (!field) {
      continue;
    }

    fields[field[1]] = field[2].trim().replace(/^["']|["']$/g, "");
  }

  return fields;
}

function section(markdown, heading) {
  const escaped = heading.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  const match = markdown.match(
    new RegExp(`(?:^|\\n)## ${escaped}\\s*\\n([\\s\\S]*?)(?=\\n## |$)`),
  );
  return match?.[1]?.trim() || "";
}

function firstParagraph(text, fallback) {
  const paragraph = text
    .split(/\n\s*\n/)
    .map((part) => part.replace(/\s*\n\s*/g, " ").trim())
    .find((part) => part && !part.startsWith("- ") && !/^\d+\.\s/.test(part));

  return paragraph || fallback;
}

function listItems(text) {
  const items = [];

  for (const line of text.split("\n")) {
    const bullet = line.match(/^\s*-\s+(.+)$/);
    const numbered = line.match(/^\s*\d+\.\s+(.+)$/);
    const item = bullet?.[1] || numbered?.[1];

    if (item) {
      items.push(item.trim());
    }
  }

  return items;
}

function titleFromId(id) {
  return id
    .split("-")
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ");
}

function toLoop(pathname, markdown) {
  const frontmatter = parseFrontmatter(markdown);
  const id =
    frontmatter.id ||
    pathname.split("/").at(-2) ||
    pathname.replace(/\/(LOOP|GOAL)\.md$/, "");
  const purpose = section(markdown, "Purpose");
  const whyUseful = section(markdown, "Why It Is Useful");
  const inputs = listItems(section(markdown, "Inputs"));
  const workflow = listItems(section(markdown, "Workflow"));
  const outputs = listItems(section(markdown, "Outputs"));
  const verifier = firstParagraph(section(markdown, "Verifier"), "See source markdown.");

  return {
    id,
    name: frontmatter.name || titleFromId(id),
    category: categoryById[id] || (pathname.startsWith("goals/") ? "Goal recipe" : "Meta systems"),
    status: frontmatter.status || "reference",
    cadence: frontmatter.cadence || "manual",
    summary: firstParagraph(purpose, "See the source markdown for the loop purpose."),
    whyUseful: firstParagraph(whyUseful || purpose, "See the source markdown for why this loop exists."),
    inputs,
    steps: workflow,
    outputs,
    verifier,
    sourcePath: `${REPO}/${pathname}`,
    sourceUrl: `https://github.com/${OWNER}/${REPO}/blob/${BRANCH}/${pathname}`,
    markdown,
  };
}

function generatedModule(loops) {
  return `// Generated by scripts/sync-georgeloops.mjs from ${OWNER}/${REPO}.\n// Do not edit loop content by hand; update GeorgeLoops and rerun npm run sync:georgeloops.\n\nexport type LoopStatus = \"active\" | \"testing\" | \"reference\" | \"idea\" | string;\n\nexport type LoopCategory =\n  | \"Daily workflow\"\n  | \"Repository ops\"\n  | \"Content\"\n  | \"Research\"\n  | \"Meta systems\"\n  | \"Goal recipe\";\n\nexport type Loop = {\n  id: string;\n  name: string;\n  category: LoopCategory;\n  status: LoopStatus;\n  cadence: string;\n  summary: string;\n  whyUseful: string;\n  inputs: string[];\n  steps: string[];\n  outputs: string[];\n  verifier: string;\n  sourcePath: string;\n  sourceUrl: string;\n  markdown: string;\n};\n\nexport const loops: Loop[] = ${JSON.stringify(loops, null, 2)};\n\nexport const categories = Array.from(new Set(loops.map((loop) => loop.category)));\nexport const statuses = Array.from(new Set(loops.map((loop) => loop.status)));\n\nexport function getLoopById(id: string) {\n  return loops.find((loop) => loop.id === id);\n}\n`;
}

async function main() {
  const tree = await githubJson(`${API_ROOT}/git/trees/${BRANCH}?recursive=1`);
  const markdownPaths = tree.tree
    .filter((entry) => {
      return (
        entry.type === "blob" &&
        (/^loops\/[^/]+\/LOOP\.md$/.test(entry.path) ||
          /^goals\/[^/]+\/GOAL\.md$/.test(entry.path))
      );
    })
    .map((entry) => entry.path)
    .sort();

  const loops = [];
  for (const markdownPath of markdownPaths) {
    const markdown = await githubText(`${RAW_ROOT}/${markdownPath}`);
    loops.push(toLoop(markdownPath, markdown));
  }

  await mkdir("lib", { recursive: true });
  await writeFile(path.join("lib", "loops.ts"), generatedModule(loops));
  console.log(`Synced ${loops.length} loop documents from ${OWNER}/${REPO}@${BRANCH}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
